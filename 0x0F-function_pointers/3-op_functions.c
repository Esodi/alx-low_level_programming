#include "calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: first integer
 * @b: second integer
 */
int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - returns the sum of a and b
 * @a: first integer
 * @b: second integer
 */
int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * op_sub - returns the sub of a and b
 * @a: first integer
 * @b: second integer
 */
int op_mul(int a, int b)
{
	return ((a) * (b));
}

/**
 * op_div - returns the div of a and b
 * @a: first integer
 * @b: second integer
 */
int op_div(int a, int b)
{
	return((a) / (b));
}

/**
 * op_mod - returns the mod of a and b
 * @a: first integer
 * @b: second integer
 */
int op_mod(int a, int b)
{
	return ((a) % (b));
}
